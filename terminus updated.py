def calculate_transfer_entropy(source, target, delay=1, order=3):
    """
    Estimates Transfer Entropy (TE) using Permutation Entropy as a proxy.
    TE measures directed information flow from source manifold to target manifold.
    """
    def joint_permutation_entropy(s, t, d, o):
        # Simplified symbolic coupling measure
        n = len(s)
        iterations = n - d * (o - 1)
        if iterations <= 0: return 0

        p_s = np.array([s[i : i + d * o : d] for i in range(iterations)])
        p_t = np.array([t[i : i + d * o : d] for i in range(iterations)])

        combined = np.hstack([p_s, p_t])
        ranks = np.argsort(combined, axis=1)
        _, counts = np.unique(ranks, axis=0, return_counts=True)
        probs = counts / counts.sum()
        return -np.sum(probs * np.log2(probs))

    # TE(S -> T) = H(T_future | T_past) - H(T_future | T_past, S_past)
    h_target = permutation_entropy(target, order, delay)
    h_joint = joint_permutation_entropy(source, target, delay, order)

    return max(0, h_target + permutation_entropy(source, order, delay) - h_joint)

