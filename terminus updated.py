class MarketDynamicalSystem:
    def __init__(self, n_steps, crash_point=800):
        self.n_steps = n_steps
        self.crash_point = crash_point

    def simulate(self):
        dt, s, r, b = 0.01, 10, 28, 8/3
        x, y, z = np.zeros(self.n_steps), np.zeros(self.n_steps), np.zeros(self.n_steps)
        x[0], y[0], z[0] = (0.1, 0.0, 0.0)

        for i in range(self.n_steps - 1):
            current_r = r if i < self.crash_point else r * (2.0 + np.sin(0.1 * (i - self.crash_point)))
            dx = s * (y[i] - x[i])
            dy = x[i] * (current_r - z[i]) - y[i]
            dz = x[i] * y[i] - b * z[i]
            x[i+1] = x[i] + dx * dt
            y[i+1] = y[i] + dy * dt
            z[i+1] = z[i] + dz * dt

        price = 100 + np.cumsum(x * 0.1)
        volatility = np.abs(y) * 0.5
        ofi = z * 100 + np.random.normal(0, 50, self.n_steps)
        impact = (np.abs(x*y) / 100) * (1 + (np.arange(self.n_steps) > self.crash_point) * 10)
        return np.column_stack([price, volatility, ofi, impact])

def compute_fisher_information(data):
    cov = np.cov(data.T)
    try:
        return np.trace(np.linalg.inv(cov + np.eye(data.shape[1])*1e-5)) / data.shape[1]
    except:
        return 0.0

