def symbolic_encoding(series, order=3):
    """Converts a time series into a sequence of permutation symbols."""
    n = len(series)
    patterns = np.array([series[i : i + order] for i in range(n - order + 1)])
    # Map each rank-order to a unique integer ID
    ranks = np.argsort(patterns, axis=1)
    symbols = [hash(tuple(r)) for r in ranks]
    unique_syms = {s: i for i, s in enumerate(set(symbols))}
    return np.array([unique_syms[s] for s in symbols])

