def generate_iaaft_surrogates(series, n_surrogates=5):
    """
    Generates IAAFT surrogates to test for non-linear determinism.
    Preserves the linear correlation (power spectrum) and distribution of the data.
    """
    print(f"[SURROGATE] Generating {n_surrogates} Phase-Randomized Null Hypotheses...")
    surrogates = []
    xf = np.fft.rfft(series)
    m_xf = np.abs(xf)
    sorted_original = np.sort(series)

    for _ in range(n_surrogates):
        phi = np.random.uniform(0, 2*np.pi, len(xf))
        phi[0] = 0
        new_xf = m_xf * np.exp(1j * phi)
        s_raw = np.fft.irfft(new_xf, n=len(series))
        s_ranks = np.argsort(np.argsort(s_raw))
        surrogates.append(sorted_original[s_ranks])
    return np.array(surrogates)

