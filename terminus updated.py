def compute_hurst_exponent(time_series, max_lag=20):
    """
    Calculates the Hurst Exponent (H) to identify long-term memory in time series.
    H < 0.5: Mean-reverting | H = 0.5: Random Walk | H > 0.5: Trending
    """
    lags = range(2, max_lag)
    tau = [np.sqrt(np.std(np.subtract(time_series[lag:], time_series[:-lag]))) for lag in lags]
    reg = np.polyfit(np.log(lags), np.log(tau), 1)
    return reg[0] * 2.0

def compute_lyapunov_exponent(series, delay=1, embed=3):
    """
    Estimates the largest Lyapunov Exponent (LLE) using a simplified Rosenstein-inspired algorithm.
    Positive values indicate chaotic dynamics.
    """
    # Phase space reconstruction
    n = len(series)
    m = n - (embed - 1) * delay
    if m <= 0: return 0

    reconstructed = np.array([series[i:i + embed * delay:delay] for i in range(m)])

    # Calculate divergence of nearest neighbors
    divergences = []
    for i in range(min(m, 100)):
        dists = np.linalg.norm(reconstructed - reconstructed[i], axis=1)
        dists[i] = np.inf
        nn_idx = np.argmin(dists)

        # Track neighbor separation (short-term)
        if i + 1 < m and nn_idx + 1 < m:
            d_initial = dists[nn_idx]
            d_after = np.linalg.norm(reconstructed[i+1] - reconstructed[nn_idx+1])
            if d_initial > 0:
                divergences.append(np.log(d_after / d_initial))

    return np.mean(divergences) if divergences else 0.0

