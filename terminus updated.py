class ManifoldEngine:
    def __init__(self, ticker, period='1y', delay=5):
        self.ticker = ticker
        self.period = period
        self.delay = delay
        self.df = None
        self.prices = None

    def fetch_data(self):
        self.df = yf.download(self.ticker, period=self.period, progress=False)
        if self.df.empty:
            raise ValueError(f"No data found for {self.ticker}")
        self.prices = self.df['Close'].values.flatten()
        return self.prices

    def get_hurst(self, series, max_lag=20):
        lags = range(2, max_lag)
        tau = [np.sqrt(np.std(np.subtract(series[lag:], series[:-lag]))) for lag in lags]
        return np.polyfit(np.log(lags), np.log(tau), 1)[0] * 2.0

    def get_lle(self, series, embed=3):
        n = len(series)
        m = n - (embed - 1) * self.delay
        if m <= 10: return 0
        reconstructed = np.array([series[i:i + embed * self.delay:self.delay] for i in range(m)])
        divergences = []
        for i in range(min(m, 50)):
            dists = np.linalg.norm(reconstructed - reconstructed[i], axis=1)
            dists[i] = np.inf
            nn_idx = np.argmin(dists)
            if i + 1 < m and nn_idx + 1 < m:
                d_initial = dists[nn_idx]
                d_after = np.linalg.norm(reconstructed[i+1] - reconstructed[nn_idx+1])
                if d_initial > 0: divergences.append(np.log(d_after / d_initial))
        return np.mean(divergences) if divergences else 0

    def get_entropy(self, series, order=3):
        patterns = np.array([series[i : i + order] for i in range(len(series) - order + 1)])
        ranks = np.argsort(patterns, axis=1)
        _, counts = np.unique(ranks, axis=0, return_counts=True)
        probs = counts / counts.sum()
        return -np.sum(probs * np.log2(probs))

    def analyze(self):
        print(f"[ENGINE] Analyzing {self.ticker}...")
        prices = self.fetch_data()
        window = prices[-120:]
        scaler = RobustScaler()
        scaled = scaler.fit_transform(window.reshape(-1, 1))

        res = rips.ripser(scaled, maxdim=1)
        h1_max = np.max(res['dgms'][1][:,1] - res['dgms'][1][:,0]) if len(res['dgms'][1]) > 0 else 0
        lle = self.get_lle(window)
        hurst = self.get_hurst(window)
        short_ent = self.get_entropy(prices[-30:])
        long_ent = self.get_entropy(prices[-90:])
        momentum = short_ent - long_ent
        fragility = (0.4 * min(lle/0.5, 1.0) + 0.3 * min(h1_max/0.8, 1.0) + 0.3 * (1-hurst)) * 100

        fig, ax = plt.subplots(1, 2, figsize=(15, 5))
        ax[0].barh(['Chaos', 'Hollowness', 'Momentum', 'Hurst'], [min(lle/0.5, 1), min(h1_max/0.8, 1), momentum, hurst], color='#2c3e50')
        ax[0].set_title(f"Signal Decomposition: {self.ticker}")
