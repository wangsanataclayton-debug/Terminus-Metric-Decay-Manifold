def permutation_entropy(time_series, order=3, delay=1):
    """Calculates Permutation Entropy as a measure of dynamic complexity."""
    n = len(time_series)
    iterations = n - delay * (order - 1)
    patterns = np.array([time_series[i : i + delay * order : delay] for i in range(iterations)])
    ranks = np.argsort(patterns, axis=1)
    _, counts = np.unique(ranks, axis=0, return_counts=True)
    probs = counts / counts.sum()
    return -np.sum(probs * np.log2(probs))

